//========================================================Services===============================================================================================================================================================-------------------------------->
angular.module('service', [])
	// each function returns a promise object 
  .factory('BMRCL', ['$http',function($http) 
	{
	return {
//*********************************************************************************************************************			
					create : function(store) {
					return $http.post('/insert',store);
				},
//*********************************************************************************************************************
					getAllUsers: function() {
					return $http.get('/retrieve');
				},
//*********************************************************************************************************************				
					emailValidation : function(email) {
					return $http({
									method: 'get',
									url: '/emailValidation',
									params: {email:email}
					});
				},
//*********************************************************************************************************************
					authenticateUser: function(uname,pass)
				{	
					return $http({
									method: 'get',
									url: '/checkuser',
									params: {uname:uname , pass:pass}
                                });   
				},
//*********************************************************************************************************************
					logout: function() {
					return $http.get('/logout');
				},
//*********************************************************************************************************************				
					sendemail: function(agencyemail){
					return $http({
							
									method: 'post',
									url: '/sendemail',
									params: {agencyemail:agencyemail}
                                }); 
				},
//*********************************************************************************************************************
					forgotpassword: function(email){
					return $http({
									
									method: 'post',
									url: '/forgotpassword',
									params: {email:email}
							
								});
				},
//*********************************************************************************************************************
					changepassword: function(email,confirmpassword){
					return $http({
									method: 'post',
									url: '/changepassword',
									params: {email:email,confirmpassword:confirmpassword}
							
								});
				}
//*********************************************************************************************************************
				/*delete : function(id) {
					return $http.delete('/api/items/' + id);
				}*/
//*********************************************************************************************************************
		}
	}]);
